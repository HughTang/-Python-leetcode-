# 是由节点在树中的位置决定。一个节点的所有子孙都有相同的前缀，也
# 就是这个节点对应的字符串，而根节点对应空字符串。一般情况下，不
# 是所有的节点都有对应的值，只有叶子节点和部分内部节点所对应的键
# 才有相关的值。
